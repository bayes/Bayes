    private void popultateColorPane(){
        GridBagConstraints gridBagConstraints ;
        JLabel textLabel, colorLabel,dummyLabel;
        for (int i = 0; i < tracecolors.size(); i++) {
            int curtrace    =   i+1;
            Color color     =   tracecolors.get(curtrace -1);
            
           
            textLabel = new javax.swing.JLabel();
            textLabel.setText("Trace "+curtrace);
            gridBagConstraints = new java.awt.GridBagConstraints();
            gridBagConstraints.gridx = 0;
            gridBagConstraints.gridy = i;
            gridBagConstraints.fill = java.awt.GridBagConstraints.HORIZONTAL;
            gridBagConstraints.insets = new java.awt.Insets(5, 5, 5, 5);
            colorPanes.add(textLabel, gridBagConstraints);
            
            colorLabel = new javax.swing.JLabel();
            colorLabel.setBackground( color); // NOI18N
            colorLabel.setOpaque(true);
            colorLabel.setPreferredSize(new java.awt.Dimension(10, 5));

            gridBagConstraints = new java.awt.GridBagConstraints();
            gridBagConstraints.gridx = 1;
            gridBagConstraints.gridy = i;
            gridBagConstraints.fill = java.awt.GridBagConstraints.BOTH;
            gridBagConstraints.insets = new java.awt.Insets(5, 5, 5, 5);
            colorPanes.add(colorLabel, gridBagConstraints);

        } 

        dummyLabel = new javax.swing.JLabel();
        gridBagConstraints = new java.awt.GridBagConstraints();
        gridBagConstraints.gridx = 0;
        gridBagConstraints.gridy = 1;
        gridBagConstraints.gridwidth = 2;
        gridBagConstraints.fill = java.awt.GridBagConstraints.BOTH;
        gridBagConstraints.weightx = 1.0;
        gridBagConstraints.weighty = 1.0;
        colorPanes.add(dummyLabel, gridBagConstraints);
        }
